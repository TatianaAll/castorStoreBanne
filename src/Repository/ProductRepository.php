    public function findBySearchQuery(?string $query): array
    {
        $qb = $this->createQueryBuilder('p');

        if ($query) {
            $qb->where('p.name LIKE :q OR p.description LIKE :q')
            ->setParameter('q', '%' . $query . '%');
        }

        return $qb->getQuery()->getResult();
